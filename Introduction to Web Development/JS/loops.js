console.log("***** For Loop *****");

for(var i=1;i<11;i++){
    console.log("2 * "+i+" = "+(2*i));
}

console.log("***** While Loop *****");

var i=1;
while(i<11){
    console.log("2 * "+i+" = "+(2*i));
    i++;
}

console.log("***** Do While Loop *****");

var i=1;
do{
    console.log("2 * "+i+" = "+(2*i));
    i++;
}while(i<11);